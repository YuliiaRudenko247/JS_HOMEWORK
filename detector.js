let result = '6' + 3 ** 0;
console.log(result); 
// Любое число, возведенное в степень 0, равно 1. Поэтому 3 ** 0 возвращает 1.
// Так как один из операндов является строкой, JavaScript преобразует число 1 
// в строку и выполняет конкатенацию. В результате получается строка '61'.
// Ожидаемый результат: '61'